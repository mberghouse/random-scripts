u0=16.428*10^-6;
a=100*10^-6;
x=linspace(-1000*10^-6,660*10^-6,3000);
y=x;
[xx,xy]=meshgrid(x);
vx=-(u0+u0*a^2*(xy.^2-xx.^2))./(xx.^2+xy.^2).^2;
vx_100=u0+u0*a^2*(a^2-a^2)/(a^2+a^2)^2;
vx_200=u0+u0*a^2*(.01*a^2-0*a^2)/(.01*a^2+0*a^2)^2;
vy=u0*a^2*2*xx*xy./(xx.^2+xy.^2).^2;
imagesc(vx)
imagesc(vx.^2+vy.^2)

vx2=-(u0+u0*a^2*(y.^2-x.^2))./(x.^2+y.^2).^2;
vy2=u0*a^2*2*x.*y./(x.^2+y.^2).^2;
imagesc(vx2.^2+vy2.^2)